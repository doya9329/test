package ch02;

public class Ex05 {
	public static void main(String[] args) {
		boolean b=true; // 논리형 true 참, false 거짓
		System.out.println(b);
		System.out.println(!b); // not 논리부정
		System.out.println(10>5);
		System.out.println(10<5);
		System.out.println(b==false); // = 할당,대입 / == 비교
		System.out.println(b!=true); // not equal
	}
}
